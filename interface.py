class UserInterface:
    """
    Модуль пользовательского интерфейса.

    Отвечает за отображение рабочей области приложения: таблицы,
    строки формул, панелей инструментов, меню и вкладок листов.
    Обеспечивает взаимодействие пользователя с системой.

    Attributes:
        theme (str): Текущая тема оформления ('light' или 'dark').
        zoom (int): Текущий масштаб отображения в процентах.
    """

    def __init__(self, theme: str = "light", zoom: int = 100):
        """
        Инициализирует модуль пользовательского интерфейса.

        Args:
            theme (str): Тема оформления — 'light' или 'dark'. По умолчанию 'light'.
            zoom (int): Масштаб отображения в процентах. По умолчанию 100.
        """
        self.theme = theme
        self.zoom = zoom

    def display_main_menu(self):
        """
        Отображает главное меню приложения.

        Выводит список доступных команд для работы с документом.
        """
        print("=== Табличный процессор ===")
        print("1. Новый файл")
        print("2. Открыть файл")
        print("3. Сохранить файл")
        print("4. Выход")

    def get_user_input(self, prompt: str) -> str:
        """
        Получает текстовый ввод от пользователя.

        Args:
            prompt (str): Текст приглашения к вводу.

        Returns:
            str: Строка, введённая пользователем.
        """
        return input(prompt)

    def display_result(self, message: str):
        """
        Отображает результат выполненной операции.

        Args:
            message (str): Текст сообщения для отображения.
        """
        print(f"[OK] {message}")

    def display_error(self, message: str):
        """
        Отображает сообщение об ошибке.

        Args:
            message (str): Текст сообщения об ошибке.
        """
        print(f"[ОШИБКА] {message}")

    def set_zoom(self, zoom: int):
        """
        Устанавливает масштаб отображения рабочей области.

        Args:
            zoom (int): Масштаб в процентах (от 10 до 400).

        Raises:
            ValueError: Если значение масштаба выходит за допустимые пределы.
        """
        if zoom < 10 or zoom > 400:
            raise ValueError("Масштаб должен быть в диапазоне от 10 до 400%")
        self.zoom = zoom
        print(f"Масштаб установлен: {self.zoom}%")
